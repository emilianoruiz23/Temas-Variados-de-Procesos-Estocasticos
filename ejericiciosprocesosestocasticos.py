# ==========================================
# EJERCICIO 4.11: Iteración de Bellman
# ==========================================
# Parámetros del modelo
m = 20           # Espacio de estados {0, 1, ..., m}
N = 50           # Horizonte temporal
p = 0.5          # Probabilidad de subir (caminata simétrica)

estados = np.arange(m + 1)

# Función de ganancia (Payoff): Parabólica convexa g(x) = (x - m/2)^2
# Premia estar en los extremos y penaliza estar en el centro.
g = (estados - m/2)**2

# Construcción de la Matriz de Transición P (con reflexión en los bordes)
P = np.zeros((m + 1, m + 1))
P[0, 1] = 1.0           # Reflexión en 0
P[m, m-1] = 1.0         # Reflexión en m
for i in range(1, m):
    P[i, i-1] = 1 - p
    P[i, i+1] = p

# Inicialización de matrices de Valor (V) y Región de Paro (D)
V = np.zeros((N + 1, m + 1))
D = np.zeros((N + 1, m + 1), dtype=bool) # True = Parar, False = Continuar

# Condición final (Horizonte N)
V[N, :] = g
D[N, :] = True # En el último instante, es obligatorio parar

# Recursión de Bellman (Hacia atrás: N-1 down to 0)
for n in range(N - 1, -1, -1):
    # Valor esperado de continuar: E[V_{n+1}(X_{n+1}) | X_n = x]
    valor_continuar = P @ V[n + 1, :]

    # Decisión óptima: max(Parar, Continuar)
    V[n, :] = np.maximum(g, valor_continuar)

    # Registrar la región de paro
    # Se detiene si la ganancia inmediata es >= al valor esperado de continuar
    D[n, :] = (g >= valor_continuar)

print("--- RESULTADOS EJERCICIO 4.11 ---")
print("Iteración de Bellman completada.")
print(f"Valor esperado máximo desde el centro (x=10) a t=0: {V[0, 10]:.4f}")

