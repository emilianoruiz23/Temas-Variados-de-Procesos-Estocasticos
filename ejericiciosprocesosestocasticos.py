def simular_cadena_embebida(T, alpha, beta, gamma, delta):
    # Tasas totales de salida para cada estado
    tasas = np.array([alpha, beta + gamma, delta])

    # Matriz de transición de la cadena embebida P(x,y)
    P = np.array([
        [0.0, 1.0, 0.0],
        [beta/(beta+gamma), 0.0, gamma/(beta+gamma)],
        [1.0, 0.0, 0.0]
    ])

    t, estado = 0.0, 0
    tiempos, estados = [0.0], [0]
    tiempo_ocupacion = np.zeros(3)

    while t < T:
        tasa_actual = tasas[estado]
        # Generar tiempo de permanencia exponencial
        sojourn = np.random.exponential(1.0 / tasa_actual)

        # Si el salto ocurre después del horizonte T, truncamos
        if t + sojourn > T:
            tiempo_ocupacion[estado] += (T - t)
            tiempos.append(T)
            estados.append(estado)
            break

        tiempo_ocupacion[estado] += sojourn
        t += sojourn

        # Saltar al siguiente estado usando la cadena embebida
        siguiente_estado = np.random.choice([0, 1, 2], p=P[estado])

        tiempos.append(t)
        estados.append(estado)
        estado = siguiente_estado

    proporciones = tiempo_ocupacion / T
    return tiempos, estados, proporciones

