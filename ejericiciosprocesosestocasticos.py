# EJERCICIO 4.12: Monte Carlo para Regla de Umbral
# ==========================================
def simular_venta_umbral(i_star, x_0, K, beta, p, m, num_sims, max_steps=500):
    ganancias_descontadas = np.zeros(num_sims)

    for sim in range(num_sims):
        x = x_0
        n = 0

        # Caminata aleatoria hasta cruzar el umbral o alcanzar límite de tiempo
        while x < i_star and n < max_steps:
            if x == 0:
                x = 1
            elif x == m:
                x = m - 1
            else:
                x += 1 if np.random.rand() < p else -1
            n += 1

        # Si cruzó el umbral antes del límite, calculamos la ganancia descontada
        if x >= i_star:
            ganancias_descontadas[sim] = (beta ** n) * (x - K)
        else:
            ganancias_descontadas[sim] = 0.0 # Nunca vendió

    return np.mean(ganancias_descontadas), np.std(ganancias_descontadas) / np.sqrt(num_sims)

# Parámetros del mercado discretizado
m_estados = 20
