import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.stats import poisson

# Configuración de estilo profesional
sns.set_theme(style="whitegrid", context="notebook")
colores_fes = ['#2C4B78', '#B8862D', '#5A6B7C']

# Parámetros del experimento
lam = 2.0                 # Intensidad original bajo P
theta = np.log(1.5)       # Cambio de medida: lam_nueva = lam * e^theta = 3.0
lam_Q = lam * np.exp(theta)
T = 5.0                   # Horizonte temporal
num_sims = 100000         # Alta cantidad de simulaciones para estabilidad de pesos

# ==========================================
# EJERCICIO 5.12: Cambio de Medida (Girsanov)
# ==========================================

print("Simulando cambio de medida... ")

# a) Simular bajo la medida original P
N_T = np.random.poisson(lam * T, num_sims)

# Calcular los pesos de la martingala (Derivada de Radon-Nikodym)
Z_T = np.exp(theta * N_T - lam * T * (np.exp(theta) - 1))

# b) Estimar distribución bajo la medida inclinada Q (Muestreo por Importancia)
max_k = int(np.max(N_T)) + 15
k_vals = np.arange(max_k)

# Probabilidad empírica bajo P (Frecuencia normal)
prob_P_emp = np.bincount(N_T, minlength=max_k) / num_sims

# Probabilidad empírica bajo Q (Frecuencia PONDERADA por Z_T)
prob_Q_emp = np.bincount(N_T, weights=Z_T, minlength=max_k) / num_sims

# Probabilidad Teórica bajo Q
prob_Q_teo = poisson.pmf(k_vals, lam_Q * T)

# c) Estudio de la varianza del estimador (Z_t) para distintos T
T_vals = np.linspace(1, 15, 15)
var_Z = []
for t_ in T_vals:
    # Simulamos para cada t
    n_t = np.random.poisson(lam * t_, num_sims)
    z_t = np.exp(theta * n_t - lam * t_ * (np.exp(theta) - 1))
    var_Z.append(np.var(z_t))

print("--- RESULTADOS EJERCICIO 5.12 ---")
print(f"Intensidad original P: {lam} | Intensidad nueva Q: {lam_Q}")
print(f"Media esperada empírica bajo Q: {np.sum(k_vals * prob_Q_emp):.4f} (Teórica: {lam_Q * T})")

# ----- GENERACIÓN DE LA GRÁFICA -----
fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 6))

# Panel 1: Cambio de Medida (Distribuciones)
ax1.bar(k_vals - 0.2, prob_P_emp, width=0.4, color=colores_fes[2], alpha=0.6, label=f'Empírica $\mathbb{{P}}$ ($\lambda={lam}$)')
ax1.bar(k_vals + 0.2, prob_Q_emp, width=0.4, color=colores_fes[0], alpha=0.8, label='Ponderada $\mathbb{{Q}}^{(\\theta)}$ ($Z_T$-weighted)')
ax1.plot(k_vals, prob_Q_teo, 'ro-', lw=2, label=f'Teórica Poisson($\lambda e^{{\\theta}}={lam_Q}$)')

ax1.set_title('Reconstrucción de Medida (Muestreo por Importancia)', fontsize=14, fontweight='bold')
