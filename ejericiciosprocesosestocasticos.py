def simular_nacimiento_muerte(T, lam, mu):
    t, estado = 0.0, 0
    tiempos, estados = [0.0], [0]
    area_integral = 0.0

    while t < T:
        # Tasa total de salida: lambda (siempre) + mu (solo si hay clientes)
        tasa_muerte = mu if estado > 0 else 0.0
        tasa_total = lam + tasa_muerte

        # Tiempo de permanencia
        sojourn = np.random.exponential(1.0 / tasa_total)

        # Truncamiento al final del horizonte temporal
        if t + sojourn > T:
            area_integral += estado * (T - t)
            tiempos.append(T)
            estados.append(estado)
            break

        area_integral += estado * sojourn
        t += sojourn

        # Decidir si es nacimiento o muerte
        prob_nacimiento = lam / tasa_total
        if np.random.rand() < prob_nacimiento:
            estado += 1
        else:
            estado -= 1

        tiempos.append(t)
        estados.append(estado)

    media_empirica = area_integral / T
    return tiempos, estados, media_empirica

