def simular_tiempos_poisson(n, lam, num_simulaciones):
    # Generar matriz de (num_simulaciones x n) variables exponenciales
    exponenciales = np.random.exponential(1.0 / lam, (num_simulaciones, n))
    # El tiempo Tn del n-ésimo salto es la suma de los n tiempos inter-arribo
    Tn = np.sum(exponenciales, axis=1)
    return Tn

