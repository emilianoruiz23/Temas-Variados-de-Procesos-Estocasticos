def trayectorias_mm_inf(x_0, lam, mu, T_max, n_paths, num_puntos=50):
    """
    Simula múltiples trayectorias de una cola M/M/infinito y
    las interpola en una malla de tiempo fija para calcular momentos.
    """
    t_grid = np.linspace(0, T_max, num_puntos)
    paths = np.zeros((n_paths, num_puntos))
    paths[:, 0] = x_0

    for i in range(n_paths):
        t, estado = 0.0, x_0
        idx = 1
        while idx < num_puntos:
            # En M/M/inf, la tasa de salida depende del número de clientes en el sistema
            tasa_salida = estado * mu
            tasa_total = lam + tasa_salida

            # Tiempo hasta el próximo evento
            sojourn = np.random.exponential(1.0 / tasa_total)

            # Registrar el estado en la malla de tiempo
            while idx < num_puntos and t_grid[idx] < t + sojourn:
                paths[i, idx] = estado
                idx += 1

            t += sojourn

            # Determinar si el evento fue llegada o salida
            if np.random.rand() < lam / tasa_total:
                estado += 1
            else:
                estado -= 1

    return t_grid, paths

