def simular_mmk_eventos(lam, mu, k, T_max):
    t = 0.0
    # Cola de prioridad para agendar eventos: (tiempo_evento, tipo_evento)
    eventos = []
    heapq.heappush(eventos, (np.random.exponential(1.0/lam), 'llegada'))

    servidores_libres = k
    cola_espera = []      # Guarda el tiempo de llegada de quienes están en fila
    tiempos_espera = []   # Guarda el tiempo total C de espera de cada cliente

    # Variables para graficar la trayectoria del sistema
    tiempos_plot = [0.0]
    clientes_sistema = [0]
    n_clientes = 0

    while eventos and t < T_max:
        t, tipo = heapq.heappop(eventos)

        if tipo == 'llegada':
            n_clientes += 1
            tiempos_plot.append(t)
            clientes_sistema.append(n_clientes)

            # Programar la PRÓXIMA llegada
            heapq.heappush(eventos, (t + np.random.exponential(1.0/lam), 'llegada'))

            if servidores_libres > 0:
                # Pasa directo al servidor
                servidores_libres -= 1
                tiempos_espera.append(0.0) # No esperó nada
                # Programar su salida
                heapq.heappush(eventos, (t + np.random.exponential(1.0/mu), 'salida'))
            else:
                # No hay servidores, se forma en la fila
                cola_espera.append(t)

        elif tipo == 'salida':
            n_clientes -= 1
            tiempos_plot.append(t)
            clientes_sistema.append(n_clientes)

            if len(cola_espera) > 0:
                # Hay gente en la fila, pasa el primero
                t_llegada = cola_espera.pop(0)
                tiempos_espera.append(t - t_llegada) # Calculamos cuánto esperó
                # Programar su salida
                heapq.heappush(eventos, (t + np.random.exponential(1.0/mu), 'salida'))
            else:
                # Nadie en la fila, se libera un servidor
                servidores_libres += 1

    return np.array(tiempos_espera), tiempos_plot, clientes_sistema

